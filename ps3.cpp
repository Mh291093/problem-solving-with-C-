// Simple Calculator - Given two numbers X and Y. Print the summation and multiplication and subtraction of these 2 numbers.
#include<bits/stdc++.h>
#include<iostream>
using namespace std;

int main () {
	long long x, y;
	
	cin >> x >> y;
	
	cout << x << " + " << y << " = " << x + y << endl;
	cout << x << " * " << y << " = " << x * y << endl;
	cout << x << " - " << y << " = " << x - y << endl;
}
